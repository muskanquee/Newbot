const config = require('../config');
const { cmd } = require('../command');
const DY_SCRAP = require('@dark-yasiya/scrap');
const dy_scrap = new DY_SCRAP();

function replaceYouTubeID(url) {
    const regex = /(?:youtube\.com\/(?:.*v=|.*\/)|youtu\.be\/|youtube\.com\/embed\/|youtube\.com\/shorts\/)([a-zA-Z0-9_-]{11})/;
    const match = url.match(regex);
    return match ? match[1] : null;
}

cmd({
    pattern: "play",
    alias: ["song", "play2"],
    react: "üéµ",
    desc: "Download Ytmp3",
    category: "download",
    use: ".song <Text or YT URL>",
    filename: __filename
}, async (conn, m, mek, { from, q, reply }) => {
    try {
        if (!q) return await reply("‚ùå Please provide a Query or Youtube URL!");

        let id = q.startsWith("https://") ? replaceYouTubeID(q) : null;

        if (!id) {
            const searchResults = await dy_scrap.ytsearch(q);
            if (!searchResults?.results?.length) return await reply("‚ùå No results found!");
            id = searchResults.results[0].videoId;
        }

        const data = await dy_scrap.ytsearch(`https://youtube.com/watch?v=${id}`);
        if (!data?.results?.length) return await reply("‚ùå Failed to fetch video!");

        const { title, image } = data.results[0];

        const msg = await conn.sendMessage(from, { text: "‚è≥ Processing audio download..." }, { quoted: mek });
        const response = await dy_scrap.ytmp3(`https://youtube.com/watch?v=${id}`);
        
        let downloadUrl = response?.result?.download?.url;
        if (!downloadUrl) return await reply("‚ùå Download link not found!");

        await conn.sendMessage(from, { 
            audio: { url: downloadUrl }, 
            mimetype: "audio/mpeg",
            fileName: `${title}.mp3`
        }, { quoted: mek });
        
        await conn.sendMessage(from, { text: '‚úÖ Audio Download Successful ‚úÖ', edit: msg.key });

    } catch (error) {
        console.error(error);
        await conn.sendMessage(from, { react: { text: '‚ùå', key: mek.key } });
        await reply(`‚ùå *An error occurred:* ${error.message || "Error!"}`);
    }
});
